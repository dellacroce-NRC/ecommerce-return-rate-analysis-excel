import pandas as pd
import numpy as np
from datetime import datetime, timedelta
import random

# 1. Catálogos para mantener integridad
regions_countries = {'EMEA': ['France', 'Germany', 'Spain', 'España'],
                     'LATAM': ['Mexico', 'México', 'Brazil', 'Brasil'],
                     'APAC': ['Australia', 'Japan', 'China'],
                     'NA': ['USA', 'United States', 'Canada']}
segments = ['Consumer', 'Corporate', 'Home Office']
categories = ['Electronics', 'Electronica', 'Furniture', 'Office Supplies', 'Oficina', 'Clothing']

customers = []
for i in range(10000):
    reg = random.choice(list(regions_countries.keys()))
    country = random.choice(regions_countries[reg])
    seg = random.choice(segments)
    customers.append({'customer_id': f'CUST-{1000+i}', 'region': reg, 'country': country, 'customer_segment': seg})
df_customers = pd.DataFrame(customers)

products = []
for i in range(50):
    cat = random.choice(categories)
    price = round(random.uniform(10, 500), 2)
    products.append({'product_id': f'P-{100+i}', 'product_category': cat, 'unit_price': price})
df_products = pd.DataFrame(products)

# 2. Transacciones (80,000 filas)
n_trans = 80000
start_date = datetime(2022, 1, 1)
end_date = datetime(2023, 12, 31)

t_customers = df_customers.sample(n_trans, replace=True).reset_index(drop=True)
t_products = df_products.sample(n_trans, replace=True).reset_index(drop=True)

dates = [start_date + timedelta(days=random.randint(0, (end_date - start_date).days)) for _ in range(n_trans)]
dates_mixed = [d.strftime('%Y-%m-%d') if random.random() > 0.05 else d.strftime('%d/%m/%Y') for d in dates]

payments = ['Credit Card', 'PayPal', 'Wire Transfer', 'Cash']
statuses = ['Delivered', 'Shipped', 'Cancelled', 'Returned']

df_trans = pd.DataFrame({
    'order_id': [f'ORD-{100000+i}' for i in range(n_trans)],
    'order_date': dates_mixed,
    'customer_id': t_customers['customer_id'],
    'region': t_customers['region'],
    'country': t_customers['country'],
    'customer_segment': t_customers['customer_segment'],
    'product_id': t_products['product_id'],
    'product_category': t_products['product_category'],
    'unit_price': t_products['unit_price'],
    'quantity': np.random.randint(1, 6, size=n_trans),
    'discount': np.where(np.random.rand(n_trans) > 0.7, np.round(np.random.uniform(0, 0.3, size=n_trans), 2), 0.0),
    'shipping_cost': np.round(np.random.uniform(5, 50, size=n_trans), 2),
    'payment_method': np.random.choice(payments, size=n_trans),
    'order_status': np.random.choice(statuses, size=n_trans, p=[0.6, 0.2, 0.1, 0.1])
})

# 3. Sesiones Web (200,000 filas)
n_sessions = 200000
devices = ['Desktop', 'Mobile', 'Tablet']
sources = ['Organic Search', 'Paid Ads', 'Direct', 'Social Media', 'Email']

s_customers = df_customers.sample(n_sessions, replace=True).reset_index(drop=True)
s_customers.loc[np.random.rand(n_sessions) < 0.15, 'customer_id'] = np.nan # 15% de invitados anónimos

# Mapear sesiones a órdenes reales
session_orders = [None] * n_sessions
mapped_indices = np.random.choice(range(n_sessions), size=n_trans, replace=False)
for idx, order_id in zip(mapped_indices, df_trans['order_id']):
    session_orders[idx] = order_id

df_sess = pd.DataFrame({
    'session_id': [f'SES-{100000+i}' for i in range(n_sessions)],
    'customer_id': s_customers['customer_id'],
    'session_date': [start_date + timedelta(days=random.randint(0, (end_date - start_date).days), hours=random.randint(0,23)) for _ in range(n_sessions)],
    'device_type': np.random.choice(devices, size=n_sessions),
    'traffic_source': np.random.choice(sources, size=n_sessions),
    'time_on_site_sec': np.random.randint(10, 1200, size=n_sessions),
    'checkout_started': np.random.choice([True, False], size=n_sessions, p=[0.4, 0.6]),
    'order_id': session_orders
})

# Arreglar consistencia de fechas y clientes para sesiones con compra
for i, row in df_trans.iterrows():
    s_idx = mapped_indices[i]
    try:
        o_date = datetime.strptime(row['order_date'], '%Y-%m-%d')
    except:
        o_date = datetime.strptime(row['order_date'], '%d/%m/%Y')
    df_sess.at[s_idx, 'session_date'] = o_date - timedelta(hours=random.randint(1, 48))
    df_sess.at[s_idx, 'checkout_started'] = True
    df_sess.at[s_idx, 'customer_id'] = row['customer_id']

# INYECCIÓN DEL PROBLEMA OCULTO (TOP SECRET)
merged = pd.merge(df_trans, df_sess[['order_id', 'traffic_source', 'device_type']], on='order_id', how='left')
# No lo diré en los comentarios para que no haya spoilers si lees esto.
mask_problem = (merged['traffic_source'] == 'Social Media') & (merged['device_type'] == 'Tablet') & (merged['product_category'].isin(['Furniture', 'Oficina']))

new_statuses = []
for i, is_prob in enumerate(mask_problem):
    if is_prob and random.random() < 0.92:
        new_statuses.append('Returned')
    else:
        new_statuses.append(df_trans.at[i, 'order_status'])
df_trans['order_status'] = new_statuses

# Inyección de suciedad y ruido para Power Query
df_trans.loc[np.random.rand(n_trans) < 0.015, 'region'] = 'NA' # El famoso NA de Norteamérica
df_sess.loc[np.random.rand(n_sessions) < 0.02, 'device_type'] = 'mobile'
df_sess.loc[np.random.rand(n_sessions) < 0.02, 'device_type'] = 'MOB'
df_trans.loc[np.random.rand(n_trans) < 0.01, 'unit_price'] = np.nan
df_trans.loc[np.random.rand(n_trans) < 0.01, 'country'] = '  ' # Espacios en blanco
df_sess.loc[np.random.rand(n_sessions) < 0.01, 'traffic_source'] = np.nan

df_trans.to_csv('transacciones_v2.csv', index=False, sep=';', encoding='utf-8-sig')
df_sess.to_csv('sesiones_web_v2.csv', index=False, sep=';', encoding='utf-8-sig')
print("Archivos generados exitosamente.")

